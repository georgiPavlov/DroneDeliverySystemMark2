CONSTRAINT FOREIGN KEY (quantity_id) REFERENCES TableProducts(id),
coordinates_id int,
CONSTRAINT FOREIGN KEY (coordinates_id) REFERENCES COORDINATES(id)
);

CREATE TABLE ORDERS_ADDS(
